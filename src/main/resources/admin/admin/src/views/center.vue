          @change="qiyeqiyefengmianUploadChange"
          ></file-upload>
        </el-form-item>
      </el-col>
      <el-col :span="12">
        <el-form-item   v-if="flag=='qiye'"  label="企业电话" prop="qiyedianhua">
          <el-input v-model="ruleForm.qiyedianhua"               placeholder="企业电话" clearable></el-input>
